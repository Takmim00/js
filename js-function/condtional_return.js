function isEven (number){
    if(number % 2 ===0){
        return true;
    }
    else{
        return false;
    }
}
console.log(isEven(5));
console.log(isEven(80));










function isOdd(number1){
    if(number1 % 2 ===1){
        return true;
    }
    else{
        return false;
    }
}
console.log(isOdd(5));
console.log(isOdd(20));






